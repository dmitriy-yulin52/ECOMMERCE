exports.deleteOrder = catchAsyncErrors(async (req, res, next) => {
  const order = await Order.findById(req.params.id);

  if (!order) {
    return next(new ErrorHandler("Заказ не найден с этим идентификатором", 404));
  }

  await order.remove();

  res.status(200).json({
    success: true,
  });
});